public class FloatValueOfNumAndReturnRoundedFloatValue {
    public static void main(String[] args) {
        double A=65.7d;
        float B= 32.5f;

        System.out.println("the rounded value " + A + "of double is: " + Math.round(A) );
        System.out.println("the rounded value " + B + "of double is: " + Math.round(B) );
    }
}
