public class DataTypes {
    public static void main(String[] args) {
        byte Num =100;
        System.out.println("value:"+Num);

        short num=5000;
        System.out.println("value:"+num);

        int number=100000;
        System.out.println("value:"+number);

        long Strength=15000000000L;
        System.out.println("value:"+Strength);

        float Percentage=76.956f;
        System.out.println("value:"+Percentage);

        double myNumber=99.560000000d;
        System.out.println("value:"+myNumber);

        boolean worldissmall=false;
        boolean worldisbig=true;
        System.out.println(worldissmall);
        System.out.println(worldisbig);

        char myGrade='A';
        System.out.println(myGrade);


    }
}
